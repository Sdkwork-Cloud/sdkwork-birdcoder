#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { pathToFileURL } from 'node:url';

import {
  readCodingServerOpenApiCodegenInput,
} from './coding-server-openapi-codegen-input.mjs';

const HTTP_METHOD_ORDER = {
  delete: 4,
  get: 0,
  patch: 3,
  post: 1,
  put: 2,
};

function readOptionValue(argv, index, flag) {
  const next = argv[index + 1];
  const normalizedNext = String(next ?? '').trim();
  if (!normalizedNext || normalizedNext.startsWith('--')) {
    throw new Error(`Missing value for ${flag}.`);
  }

  return normalizedNext;
}

function defaultOutputFile() {
  return path.join(
    process.cwd(),
    'packages',
    'sdkwork-birdcoder-types',
    'src',
    'generated',
    'coding-server-openapi.ts',
  );
}

export function parseArgs(argv) {
  const options = {
    outputFile: defaultOutputFile(),
    releaseAssetsDir: path.join(process.cwd(), 'artifacts', 'release'),
  };

  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];
    if (token === '--release-assets-dir') {
      options.releaseAssetsDir = path.resolve(readOptionValue(argv, index, '--release-assets-dir'));
      index += 1;
      continue;
    }

    if (token === '--output-file') {
      options.outputFile = path.resolve(readOptionValue(argv, index, '--output-file'));
      index += 1;
      continue;
    }
  }

  return options;
}

function inferSurface(pathKey, fallbackTag) {
  const normalizedTag = String(fallbackTag ?? '').trim();
  if (normalizedTag) {
    return normalizedTag;
  }

  if (pathKey.startsWith('/api/core/')) {
    return 'core';
  }

  if (pathKey.startsWith('/api/app/')) {
    return 'app';
  }

  if (pathKey.startsWith('/api/admin/')) {
    return 'admin';
  }

  return 'unknown';
}

export function collectCodingServerOpenApiOperations(document) {
  const operations = [];
  const pathEntries = Object.entries(document?.paths ?? {});

  for (const [pathKey, methodMap] of pathEntries) {
    for (const [methodKey, operationDefinition] of Object.entries(methodMap ?? {})) {
      const normalizedMethod = String(methodKey ?? '').trim().toLowerCase();
      if (!(normalizedMethod in HTTP_METHOD_ORDER)) {
        continue;
      }

      const tags = Array.isArray(operationDefinition?.tags)
        ? operationDefinition.tags.map((entry) => String(entry ?? '').trim()).filter(Boolean)
        : [];

      operations.push({
        method: normalizedMethod.toUpperCase(),
        operationId: String(operationDefinition?.operationId ?? '').trim(),
        path: pathKey,
        summary: String(operationDefinition?.summary ?? '').trim(),
        surface: inferSurface(pathKey, tags[0]),
      });
    }
  }

  operations.sort((left, right) => {
    if (left.path !== right.path) {
      return left.path.localeCompare(right.path);
    }

    return (HTTP_METHOD_ORDER[left.method.toLowerCase()] ?? 99)
      - (HTTP_METHOD_ORDER[right.method.toLowerCase()] ?? 99);
  });

  return operations;
}

export function buildCodingServerOpenApiTypesSource({
  evidence,
  operations,
  releaseTag,
} = {}) {
  const evidenceSource = JSON.stringify({
    canonicalRelativePath: evidence.canonicalRelativePath,
    mirroredRelativePaths: evidence.mirroredRelativePaths,
    openapi: evidence.openapi,
    releaseTag,
    sha256: evidence.sha256,
    targetCount: evidence.targetCount,
    targets: evidence.targets,
    title: evidence.title,
    version: evidence.version,
  }, null, 2);
  const operationsSource = JSON.stringify(operations, null, 2);

  return `// Generated by scripts/generate-coding-server-openapi-types.mjs from finalized release manifest. Do not edit by hand.

export const BIRDCODER_FINALIZED_CODING_SERVER_OPENAPI_EVIDENCE = ${evidenceSource} as const;

export const BIRDCODER_FINALIZED_CODING_SERVER_OPENAPI_OPERATIONS = ${operationsSource} as const;

export type BirdCoderFinalizedCodingServerOpenApiEvidence =
  typeof BIRDCODER_FINALIZED_CODING_SERVER_OPENAPI_EVIDENCE;

export type BirdCoderFinalizedCodingServerOpenApiOperation =
  typeof BIRDCODER_FINALIZED_CODING_SERVER_OPENAPI_OPERATIONS[number];

export type BirdCoderFinalizedCodingServerOpenApiOperationId =
  BirdCoderFinalizedCodingServerOpenApiOperation['operationId'];

export type BirdCoderFinalizedCodingServerOpenApiSurface =
  BirdCoderFinalizedCodingServerOpenApiOperation['surface'];
`;
}

export function generateCodingServerOpenApiTypes({
  outputFile = defaultOutputFile(),
  releaseAssetsDir = path.join(process.cwd(), 'artifacts', 'release'),
} = {}) {
  const codegenInput = readCodingServerOpenApiCodegenInput({
    releaseAssetsDir,
  });
  const document = JSON.parse(fs.readFileSync(codegenInput.canonicalSnapshotPath, 'utf8'));
  const operations = collectCodingServerOpenApiOperations(document);
  const source = buildCodingServerOpenApiTypesSource({
    evidence: codegenInput,
    operations,
    releaseTag: codegenInput.releaseTag,
  });
  const normalizedOutputFile = path.resolve(outputFile);

  fs.mkdirSync(path.dirname(normalizedOutputFile), { recursive: true });
  fs.writeFileSync(normalizedOutputFile, source);

  return {
    canonicalRelativePath: codegenInput.canonicalRelativePath,
    operationCount: operations.length,
    outputFile: normalizedOutputFile,
    releaseTag: codegenInput.releaseTag,
  };
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  const result = generateCodingServerOpenApiTypes(parseArgs(process.argv.slice(2)));
  console.log(JSON.stringify(result, null, 2));
}
