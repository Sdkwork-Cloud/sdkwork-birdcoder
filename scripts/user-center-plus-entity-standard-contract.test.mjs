function bodyMatchesColumnType(body, columnName, columnDefinition) {
  return new RegExp(
    `\\b${escapeRegExp(columnName)}\\s+${escapeRegExp(columnDefinition)}(?=\\s|,|$)`,
    'i',
  ).test(body);
}

